# What is frappe?

#     open-source web application development framework written in Python
#     building enterprise applications
#     uses meta data as building blocks
#     these meta data are used to create database models

# why frappe?
 
#     helps to build applications faster
#     uses DOM concepts with makes render faster
#     build in UI components which makes it easier rather than building from scratch
#     has role based access control so that it restrict data based on user type
#     open source and has huge community
#     provides restfull api's that making it easier to integrate with other systems

# Mainly used in?

#     building ERP systems like jira
#     web development
#     E-commerce platforms
#     Automation systems
