# different types of print statements

# basic print
print('hello')

# printing values of variables
a=10
b=20
print('sum of ',a,'+',b,'=',a+b)

# format
name="bibin"
age=23
print('I am {}, {} years old'.format(name,age))

# concantenation
print('I am '+str(name)+', '+str(age)+' years old')

# separator
print(name,age,sep=' - ')

# end parameter
print('Hi everyone !!!',end=' ')
print('How are you all ??')

print('gkhdsdksh',end='.')
